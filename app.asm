org 0
cpu 8086

COLS        equ 80
ROWS        equ 50
CELL_BYTES  equ 4
SCREEN_SIZE equ COLS * ROWS * CELL_BYTES

setup:
  cli
  cld
  mov ax,cs
  mov ds,ax
  mov ss,ax
  mov sp,0fffeh
  sti

  call setup_video
  call generate_sin_table

  ; Visit every 8x4-pixel cell in a diagonal order.  This leaves a visible
  ; trail while the plasma phase advances, as in app3-plasma.asm.
  xor di,di

draw:
  ; Combine three independent sine waves.  Their separate phase speeds avoid
  ; the obvious repeating diagonal bands of a single moving wave.
  mov al,[cell_row]
  mov cl,al
  shl al,1
  shl al,1
  add al,cl                     ; row * 5
  add al,[phase_y]
  mov cl,al                     ; retain row phase for the diagonal wave
  mov bx,sin_table
  xlat
  mov dh,al

  mov al,[cell_col]
  shl al,1                      ; column * 2: less than one wave across 80 cells
  add al,[phase_x]
  xlat
  add al,dh
  mov dh,al

  ; A diagonal component breaks up the remaining regular row/column grid.
  mov al,[cell_col]
  add al,cl
  add al,[phase_diag]
  xlat
  add al,dh
  shr al,1
  shr al,1
  shr al,1
  mov bx,img_offsets
  xor ah,ah
  shl ax,1
  add bx,ax
  mov si,[bx]
  add si,img

  call draw_cell

  add di,[step]
  inc byte [cell_col]
  mov al,16
  cmp byte [cell_col],COLS
  jb .advance_row
  mov byte [cell_col],0
  inc al
.advance_row:
  add byte [cell_row],al
  cmp byte [cell_row],ROWS
  jb draw
  sub byte [cell_row],ROWS
  sub di,SCREEN_SIZE
  ; Keep a phase stable for 256 complete diagonal passes.  A new phase is
  ; therefore nearly fully drawn before it differs enough to be noticeable.
  inc word [passes]
  test word [passes],01ffh
  jnz draw
  inc byte [phase_x]
  inc byte [phase_y]
  add byte [phase_diag],2
  jmp draw

; Copy one 8x4 RGB cell from DS:SI to the three video planes at ES:DI.
; The source layout is four bytes each for red, green, then blue.
draw_cell:
  mov bx,di

  mov ax,RED_SEG
  mov es,ax
  movsw
  movsw

  mov di,bx
  mov ax,GREEN_SEG
  mov es,ax
  movsw
  movsw

  mov di,bx
  mov ax,BLUE_SEG
  mov es,ax
  movsw
  movsw

  mov di,bx
  ret

; Expand one 64-entry quarter sine into a 256-entry, unsigned sine table.
generate_sin_table:
  push es
  push cs
  pop es
  mov di,sin_table
  xor bx,bx
  mov cx,256
.loop:
  mov al,bl
  and al,63
  test bl,64
  jz .index_ready
  xor al,63
.index_ready:
  xor ah,ah
  mov si,qsin_table
  add si,ax
  mov al,[si]
  test bl,128
  jz .positive
  neg al
.positive:
  add al,128
  stosb
  inc bl
  loop .loop
  pop es
  ret

phase_x: db 0
phase_y: db 0
phase_diag: db 0
passes: dw 0
cell_row: db 0
cell_col: db 0
step: dw CELL_BYTES + 16 * COLS * CELL_BYTES

